
public class Skills {

	String name;
	String effect;
	double effectNumber;
	
	public Skills() {
		name = "null";
		effect = "null";
		effectNumber = 0;
	}
	
	public String getName() {
		return name;
	}
	
	public String getEffect() {
		return effect;
	}

	public double getEffectNumber() {
		return effectNumber;
	}	
	
}
